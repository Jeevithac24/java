class CharRunner{
	public static void main(String[] args){
		char name='J';
		System.out.println("name of the person is"+name);
		char gradeOfHarshitha='C';
		System.out.println("grade Of Harshitha is"+gradeOfHarshitha);
		char gradeOfJyashree='A';
		System.out.println("grade Of Jyashree is"+gradeOfJyashree);
		char percentage='%';
		System.out.println("percentage symbol is"+percentage);
		char dollarSymbol='$';
		System.out.println("dollar Symbol is"+dollarSymbol);
		char dotOperator='.';
		System.out.println("dot Operator is"+dotOperator);
		char choice='D';
		System.out.println("choice is"+choice);
		char label='#';
		System.out.println("label is represented by"+label);
		char multiplication='*';
		System.out.println("multiplication is"+multiplication);
		char mention='@';
		System.out.println("mention is"+mention);
		
	}
}